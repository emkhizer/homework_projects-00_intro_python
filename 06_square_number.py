# Ask the user for a number and print its square 
# (the product of the number times itself).

# User se ek number input lena
number = float(input("Enter a number: "))  # User se number input lena

# Number ka square calculate karna
square = number * number  # Square = number * number

# Square ko print karna
print(f"The square of {number} is: {square}")
# `f-string` ka use karke number aur square ko message ke sath show kiya jata
